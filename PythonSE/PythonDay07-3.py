"""
公鸡5元一只，母鸡3元一只，小鸡1元三只，用100块钱买一百只鸡，问公鸡、母鸡、小鸡各有多少只？
穷举
"""
# 设公鸡有X只，范围是0~20只
for x in range(0, 21):
    # 母鸡有y只，范围是0~33只
    for y in range(0, 34):
        # 雏鸡数量z=100-x-y
        z = 100 - x - y
        if x * 5 + y * 3 + z // 3 == 100 and z % 3 == 0:
            print(f'公鸡有：{x}只，母鸡有：{y}只，雏鸡有：{z}只')
